package JavaExamPractice;

public class ForLoop5thNumber {

	public static void main(String[] args) {
		// Write a for loop for every 5th # between 0 & 100
		
		for (int i = 0; i <=100; i+=5) {
			System.out.println(i);
			
		}

		// Write a for loop for every 10th # between 0 & 100
		for(int i=0; i<=100; i+=10) {
			
			System.out.println("Every 10th # is "+i);
		}
	}

}
